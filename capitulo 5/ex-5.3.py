# Exercício 5.3  :: Crie uma função que tenha 3 parâmetros: um número inteiro, um número real e um texto. A função deve imprimir os valores dos parâmetros na tela. Em seguida, chame a função passando os argumentos de forma posicional.

def imprimir_parametros(numero_inteiro, numero_real, texto):
    print("Número inteiro:", numero_inteiro)
    print("Número real:", numero_real)
    print("Texto:", texto)

imprimir_parametros(10, 3.14, "Python é divertido!")
